    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String username = jTextField1.getText().trim();
        String password = new String(jPasswordField1.getPassword());
        password = password.trim();

        Employee emp = gui.getUserAuthorization(username, password);
        if (emp == null) {
            showMessageDialog(null, "Invalid username and/or password.\nTry again.", "Login", ERROR_MESSAGE);
        }
        if(emp != null){
			gui.employee_id = emp.getEmplID();
            switch(emp.getUsertype()){
                case 0 :
                    gui.byttVindu(this, new MainMenu(gui));
                    break;
                    
                case 1 : 
                    gui.byttVindu(this, new SalesMain(gui));
                    break;
                case 2:
                    gui.byttVindu(this, new Storage(gui));
                    break;
                case 3:
                    gui.byttVindu(this, new Storage(gui));
                    break;
                case 4:
                    gui.byttVindu(this, new Statistics(gui));
                    break;
                default:
                break;
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

