    public boolean verifyFields(String oldPass, String newPass1, String newPass2, Employee emp) {
        if (emp == null) {
            showMessageDialog(null, "Invalid username and/or password.\nTry again.", "Password manager", ERROR_MESSAGE);
            return false;
        } else if (oldPass == null || oldPass.equals("") || newPass1 == null || newPass2 == null) {
            showMessageDialog(null, "No fields can be empty.", "Password manager", ERROR_MESSAGE);
            return false;
        } else if (newPass1.equalsIgnoreCase(oldPass)) {
            showMessageDialog(null, "New password cannot be the same as the old one", "Password manager", ERROR_MESSAGE);
            return false;
        } else if (!newPass1.equals(newPass2)) {
            showMessageDialog(null, "Enter identical passwords in the 'New password'-fields", "Password manager", ERROR_MESSAGE);
            return false;
        } else if (!emp.changePassword(oldPass, newPass1)) {
            showMessageDialog(null, "Password cannot exceed 20 symbols", "Password manager", ERROR_MESSAGE);
            return false;
        }
        return true;
    }

