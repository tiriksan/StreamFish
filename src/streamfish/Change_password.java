    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String username = jTextField1.getText();
        String password = new String(jPasswordField1.getPassword());
        password = password.trim();
        String newPass1 = new String(jPasswordField2.getPassword());
        String newPass2 = new String(jPasswordField3.getPassword());
        Employee emp = gui.getUserAuthorization(username, password);
        
        if (verifyFields(password, newPass1, newPass2, emp)) {
            if (gui.changePassword(emp)) {
                this.dispose();
            } else {
                System.err.println("Something went wrong while changing password.");
            }
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

